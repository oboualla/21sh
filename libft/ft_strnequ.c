#include "libft.h"

int		ft_strnequ(char *s1, char *s2, size_t n)
{
	size_t i;

	i = 0;
	if (n == 0)
		return (1);
	if (s1 != NULL && s2 != NULL && n != 0)
	{
		while (i < n)
		{
			if (s1[i] != s2[i])
			{
				return (0);
			}
			i++;
		}
		return (1);
	}
	return (0);
}
