void	ft_rev(char *str)
{
	int		i;
	char	temp;
	int		j;

	j = 0;
	i = 0;
	while (str[j])
		j++;
	j--;
	while (i < j)
	{
		temp = str[i];
		str[i] = str[j];
		str[j] = temp;
		j--;
		i++;
	}
}
