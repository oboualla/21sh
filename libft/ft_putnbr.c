#include "libft.h"

void		ft_putnbr(int i)
{
	unsigned int		n;
	char				a;

	if (i < 0)
	{
		write(1, "-", 1);
		i = i * -1;
	}
	n = i;
	if (n < 10)
	{
		a = n + 48;
		write(1, &a, 1);
	}
	else
	{
		ft_putnbr(n / 10);
		a = n % 10 + '0';
		write(1, &a, 1);
	}
}
