#include "libft.h"

void	free_double(char ***tab)
{
	int i;

	i = 0;
	if (!tab || !*tab)
		return ;
	while ((*tab)[i])
		ft_strdel(&(*tab)[i++]);
	free(*tab);
	*tab = NULL;
}
