#include "libft.h"

int	ft_is_separ(char const *str, char c)
{
	int i;

	if (!(str))
		return (0);
	i = 0;
	while (str[i] != '\0' && str[i] != c)
		i++;
	if (str[i] == c)
		return (1);
	return (0);
}
